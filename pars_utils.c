void	skip_spaces(char **s)
{
	while (**s == ' ' && **s)
		(*s)++;
}

void	check_trash(char *s)
{
	while (*s)
	{
		if (*s != ' ')
			write(1, "error\n", 6);
		s++;
	}
}
